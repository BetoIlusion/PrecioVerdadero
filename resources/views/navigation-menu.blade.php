<script src="{{ asset('js/app.js') }}"></script>
<script>
    let notificationCount = {{ $user ? $user->notifications()->where('read', false)->count() : 0 }};

    function updateNotificationCount() {
        document.getElementById('notification-count').textContent = notificationCount;
        document.getElementById('notification-count-mobile').textContent = notificationCount;
    }

    window.Echo.channel('mercader.{{ auth()->id() }}')
        .listen('ActividadCreated', (e) => {
            notificationCount++;
            updateNotificationCount();
            const notificationArea = document.createElement('div');
            notificationArea.className = 'bg-green-100 border border-green-400 text-green-700 px-4 py-3 rounded fixed top-4 right-4 z-50';
            notificationArea.innerHTML = `
                <strong>Notificación:</strong> ${e.message}
                <span class="absolute top-0 right-0 px-2 py-1 cursor-pointer" onclick="this.parentElement.remove()">X</span>
            `;
            document.body.appendChild(notificationArea);
            setTimeout(() => notificationArea.remove(), 5000);
        });
</script>